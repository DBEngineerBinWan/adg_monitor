    PARTITION p_202712 VALUES LESS THAN (TIMESTAMP '2028-01-01 00:00:00'),
    PARTITION p_202801 VALUES LESS THAN (TIMESTAMP '2028-02-01 00:00:00'),
    PARTITION p_202802 VALUES LESS THAN (TIMESTAMP '2028-03-01 00:00:00'),
    PARTITION p_202803 VALUES LESS THAN (TIMESTAMP '2028-04-01 00:00:00'),
    PARTITION p_202804 VALUES LESS THAN (TIMESTAMP '2028-05-01 00:00:00'),
    PARTITION p_maxval VALUES LESS THAN (MAXVALUE)
);

-- 触发器 (自增 ID)
CREATE OR REPLACE TRIGGER TRG_ADG_HISTORY_ID
BEFORE INSERT ON ADG_MONITOR_HISTORY
FOR EACH ROW
BEGIN
    SELECT SEQ_ADG_HISTORY.NEXTVAL INTO :NEW.ID FROM DUAL;
END;
/

-- LOCAL 分区索引
CREATE INDEX IDX_HISTORY_DBID_TIME ON ADG_MONITOR_HISTORY(DB_ID, COLLECT_TIME DESC) LOCAL;

CREATE INDEX IDX_HISTORY_TIME ON ADG_MONITOR_HISTORY(COLLECT_TIME DESC) LOCAL;

