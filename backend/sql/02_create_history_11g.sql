    PARTITION p_202611 VALUES LESS THAN (TIMESTAMP '2026-12-01 00:00:00'),
    PARTITION p_202612 VALUES LESS THAN (TIMESTAMP '2027-01-01 00:00:00'),
    PARTITION p_202701 VALUES LESS THAN (TIMESTAMP '2027-02-01 00:00:00'),
